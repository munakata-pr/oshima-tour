# --- Plan helper ---
def add_plan(color_label, title, desc_paragraphs, info_lines):
    doc.add_heading(f'{color_label}　{title}', level=3)
    for desc in desc_paragraphs:
        doc.add_paragraph(desc)
    # Info block
    p = doc.add_paragraph()
    for i, line in enumerate(info_lines):
        run = p.add_run(line)
        run.font.size = Pt(10)
        if i < len(info_lines) - 1:
            p.add_run('\n').font.size = Pt(10)

